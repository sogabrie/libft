char *ft_strrchr(const char *s, int c)
{
	char *a;
	char *a1;

	a = (char *) s;
	a1 =(char *) 0;
	while (*a != 0)
	{
		if (*a == c)
			a1 = a;
		++a;
	}
	return (a1);
}
