#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	*a;

	i = 0;
	a = ft_itoa(n);
	while (a[i])
	{
		write(fd, &a[i], 1);
		++i;
	}
}
